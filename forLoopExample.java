class forLoopExample
{
	public static void main(String args[])
	{
		for(int i=5; i>0; i--) 
		{
			for(int j=i; j>0; j--)
			{
				System.out.print("*");
			}
			if (i >= 1)
				{
					System.out.println("");
				}
		}
		
		for(int a = 1; a <=5; a ++)
		{
			for(int b = a; b<5; b++)
			{
				System.out.print("*");
			}
			if (a <= 5) 
			{
				System.out.println("");
			}
			
		}
		
		
	
	}

}
